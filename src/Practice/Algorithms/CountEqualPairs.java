package Practice.Algorithms;
/**
 * ============================================================
 * ЗАДАЧА: Подсчёт количества равных пар из двух массивов
 * ============================================================
 *
 * Условие:
 * Даны два отсортированных массива a и b.
 * Нужно посчитать, сколько существует пар (i, j),
 * таких что a[i] == b[j].
 *
 * Тема:
 * - Two Pointers
 * - Обработка дубликатов
 *
 * Идея:
 * 1) Двигаем два указателя i и j.
 * 2) Если значения разные — двигаем указатель меньшего.
 * 3) Если значения равны:
 *    - считаем, сколько раз x подряд встречается в a (countA)
 *    - считаем, сколько раз x подряд встречается в b (countB)
 *    - добавляем countA * countB к ответу
 *
 * Почему работает:
 * - Массивы отсортированы → одинаковые элементы идут подряд.
 * - Каждую группу одинаковых чисел обрабатываем за один проход.
 *
 * Асимптотика:
 * Время: O(n + m)
 * Память: O(1)
 *
 * Важно / pitfalls:
 * - Массивы должны быть отсортированы.
 * - ans должен быть long (в коде сейчас int — возможен overflow).
 *   Например, 100_000 * 100_000 выйдет за пределы int.
 * ============================================================
 */
@SuppressWarnings("ALL")
public class CountEqualPairs {
    public static void main(String[] args) {
        int[] a = {1, 1, 1, 1, 1, 1, 1, 1, 1};
        int[] b = {1, 1, 1, 1, 1, 1, 1, 1, 1};

        System.out.println(countEqualPairs(a, b));
    }

    public static long countEqualPairs(int[] a, int[] b) {
        int i = 0, j = 0;
        long count = 0;
        while (i < a.length && j < b.length) {
            if (a[i] < b[j]) i++;
            else if (a[i] > b[j]) j++;
            else {
                int x = a[i];
                long countA = 0, countB = 0;

                while (i  < a.length && x == a[i]) { countA++; i++; }
                while (j < b.length && x == b[j]) { countB++; j++; }

                count += countA * countB;
            }
        }
        return count;
    }
}

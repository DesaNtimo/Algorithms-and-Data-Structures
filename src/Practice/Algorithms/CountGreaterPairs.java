package Practice.Algorithms;
/**
 * ============================================================
 * ЗАДАЧА: Подсчёт количества пар (a[i], b[j]), где b[j] < a[i]
 * ============================================================
 *
 * Условие:
 * Даны два отсортированных по возрастанию массива a и b.
 * Нужно посчитать количество пар (i, j),
 * таких что b[j] < a[i].
 *
 * Тема:
 * - Two Pointers
 * - Монотонное продвижение указателя
 *
 * Идея:
 * 1) Для каждого элемента i из массива a
 * 2) Продвигаем указатель j в массиве b,
 *    пока b[j] < i.
 * 3) Все элементы b[0..j-1] подходят → добавляем j к ответу.
 *
 * Почему работает:
 * - Массивы отсортированы.
 * - j никогда не откатывается назад → общий проход линейный.
 *
 * Асимптотика:
 * Время: O(n + m)
 * Память: O(1)
 *
 * Важно:
 * - Массивы должны быть отсортированы.
 * - j объявлен вне цикла → ключ к линейной сложности.
 * - Если бы j сбрасывался для каждого i → было бы O(n*m).
 * ============================================================
 */
@SuppressWarnings("ALL")
public class CountGreaterPairs {
    public static void main(String[] args) {
        int[] a = {1, 2, 3, 4, 5, 6, 7, 8, 9};
        int[] b = {-1, 0, 1, 4, 5, 6, 7, 8, 9};

        System.out.println(countGreaterPairs(a, b));
    }

    public static long countGreaterPairs(int[] a, int[] b) {
        int j = 0; long count = 0;
        for (int i : a) {
            while (j < b.length && b[j] < i) j++;
            count += j;
        }
        return count;
    }
}

package Practice.Algorithms;
import java.util.*;
/**
 * ============================================================
 * ЗАДАЧА: Поиск пары из двух отсортированных массивов с заданной суммой
 * ============================================================
 *
 * Условие:
 * Даны два отсортированных по возрастанию массива a и b.
 * Найти элементы a[i] и b[j], такие что a[i] + b[j] = target.
 *
 * Тема:
 * - Two Pointers (два указателя)
 * - Работа с отсортированными массивами
 *
 * Идея:
 * 1) Указатель i — в начале массива a.
 * 2) Указатель j — в конце массива b.
 * 3) Если сумма больше target → уменьшаем j.
 * 4) Если сумма меньше target → увеличиваем i.
 * 5) Если равна — возвращаем пару.
 *
 * Почему работает:
 * - Массивы отсортированы.
 * - Движение указателей монотонно уменьшает пространство поиска.
 *
 * Асимптотика:
 * Время: O(n + m)
 * Память: O(1)
 *
 * Важно:
 * - Массивы ОБЯЗАНЫ быть отсортированы.
 * - Приведение к long защищает от переполнения int.
 * - Возвращаются значения, а не индексы (по условию реализации).
 * ============================================================
 */
public class FindSum {
    public static void main(String[] args) {
        int[] arr = new int[]{1, 2, 3, 4, 5, 6, 7, 8, 9};
        int target = 7;
        int[] mas = new int[]{1, 2, 3, 4, 5, 6, 7, 8, 9};

        System.out.println(Arrays.toString(findSum(arr, mas, target)));
    }

    public static int[] findSum(int[] a, int[] b, int target) {
        int i = 0, j = b.length - 1;
        while (i < a.length && j >= 0) {
            long sum = (long) a[i] + (long) b[j];
            if (sum == target) return new int[]{a[i], b[j]};
            if (sum > target) j--;
            else i++;
        }
        return new int[]{-1, -1};
    }
}

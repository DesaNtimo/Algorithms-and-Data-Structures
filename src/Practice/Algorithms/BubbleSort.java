package Practice.Algorithms;

import java.util.Arrays;
/**
 * ============================================================
 * ЗАДАЧА: Сортировка массива методом пузырька (Bubble Sort)
 * ============================================================
 *
 * Условие:
 * Дан массив целых чисел. Отсортировать его по возрастанию.
 *
 * Тема:
 * - Сортировки
 * - Квадратичные алгоритмы
 *
 * Идея:
 * 1) Проходим по массиву несколько раз.
 * 2) На каждом проходе сравниваем соседние элементы.
 * 3) Если левый больше правого — меняем местами.
 * 4) После i-го прохода i последних элементов уже на своих местах.
 *
 * Асимптотика:
 * Время:
 *   Худший/средний случай — O(n²)
 *   Лучший случай         — O(n²) (в этой реализации)
 * Память: O(1)
 *
 * Важно:
 * - Алгоритм стабильный.
 * - Очень медленный для больших n.
 * - Можно оптимизировать флагом swapped (если не было обменов — массив отсортирован).
 * ============================================================
 */
@SuppressWarnings("ALL")
public class BubbleSort {
    public static void main(String[] args) {
        int[] arr = {9, 5, 3, 7, 4, 56, 23, 11};
        bubbleSort(arr);
        System.out.println(Arrays.toString(arr));
    }

    public static void bubbleSort(int[] arr) {
        int n = arr.length;
        for (int i = 0; i < n - 1; i++) {
            for (int j = 0; j < n - 1 - i; j++) {
                if (arr[j] > arr[j + 1]) {
                    int temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                }
            }
        }
    }
}

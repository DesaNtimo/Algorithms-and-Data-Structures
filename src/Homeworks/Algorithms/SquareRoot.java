package Homeworks.Algorithms;

import java.io.*;
/**
 * ===========================================================
 * ЗАДАЧА: Решение уравнения методом бинарного поиска
 * ===========================================================
 * <p>
 * Условие:
 * Дано число target. Нужно найти x ≥ 0 такое, что
 * x² + √(x + 1) = target.
 * <p>
 * Ответ требуется вывести с высокой точностью.
 * <p>
 * Тема:
 * - Бинарный поиск по вещественному ответу
 * - Численные методы
 * <p>
 * Идея:
 * 1) Рассматриваем функцию f(x) = x² + √(x + 1).
 * 2) Эта функция монотонно возрастает при x ≥ 0.
 * 3) Значит можно применить бинарный поиск.
 * 4) Сужаем диапазон [lo, hi], пока не получим нужную точность.
 * <p>
 * Почему работает:
 * - f(x) непрерывна и строго возрастает.
 * - Следовательно, уравнение имеет единственный корень.
 * - Бинарный поиск сходится к решению.
 * <p>
 * Асимптотика:
 * Время: O(K), где K ≈ 200 итераций
 * Память: O(1)
 * <p>
 * Важно:
 * - hi выбирается как √target + 2, чтобы гарантированно покрыть корень.
 * - 200 итераций достаточно для точности double.
 * - Вывод форматируется с точностью 10 знаков после запятой.
 * ===========================================================
 */
public class SquareRoot {
    private static double f(double x) {
        return x * x + Math.sqrt(x + 1.0);
    }

    public static void main(String[] args) throws Exception {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        double target = Double.parseDouble(br.readLine().trim());

        double lo = 0.0;
        double hi = Math.sqrt(target) + 2.0;

        for (int it = 0; it < 200; it++) {
            double mid = (lo + hi) / 2.0;
            if (f(mid) < target) lo = mid;
            else hi = mid;
        }

        System.out.printf("%.10f%n", hi);
    }
}

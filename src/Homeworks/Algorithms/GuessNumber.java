package Homeworks.Algorithms;

import java.io.*;
/**
 * ===========================================================
 * ЗАДАЧА: Угадай число (интерактивная задача)
 * ===========================================================
 * <p>
 * Условие:
 * Загадано число в диапазоне [1..n].
 * Программа может задавать вопросы, выводя число mid.
 * Судья отвечает:
 * "<" — если загаданное число меньше mid,
 * ">=" — если число больше или равно mid.
 * Нужно определить загаданное число.
 * <p>
 * Тема:
 * - Бинарный поиск
 * - Интерактивные задачи
 * <p>
 * Идея:
 * 1) Поддерживаем диапазон возможного ответа [lo, hi].
 * 2) Спрашиваем середину mid.
 * 3) Если ответ "<" → число меньше → hi = mid - 1.
 * 4) Иначе → число ≥ mid → lo = mid.
 * 5) Когда lo == hi — число найдено.
 * <p>
 * Почему работает:
 * - Каждый вопрос уменьшает диапазон поиска в ~2 раза.
 * - Это классический бинарный поиск по ответу.
 * <p>
 * Асимптотика:
 * Вопросов: O(log n)
 * Память: O(1)
 * <p>
 * Важно:
 * - Используется mid = (lo + hi + 1) / 2,
 *   чтобы избежать зацикливания при двух элементах.
 * - После каждого вывода вызывается flush(),
 *   иначе судья может не получить запрос.
 * - В конце ответ выводится в формате "! X".
 * ===========================================================
 */
public class GuessNumber {
    public static void main(String[] args) throws Exception {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));

        long lo = 1, hi = Long.parseLong(br.readLine().trim());

        while (lo < hi) {
            long mid = (lo + hi + 1) / 2;
            System.out.println(mid);
            System.out.flush();

            String ans = br.readLine();
            if (ans == null) return;
            ans = ans.trim();

            if (ans.equals("<")) {
                hi = mid - 1;
            } else {
                lo = mid;
            }
        }

        System.out.println("! " + lo);
        System.out.flush();
    }
}

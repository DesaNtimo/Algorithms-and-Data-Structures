package Homeworks.Algorithms;

import java.io.*;
/**
 * ===========================================================
 * ЗАДАЧА: Количество проходов Bubble Sort после последовательных вставок
 * ===========================================================
 * <p>
 * Условие:
 * Дана перестановка, которая строится по одному элементу:
 * на каждом шаге поступает позиция pos, куда ставится очередной элемент.
 * После каждого шага нужно определить,
 * сколько проходов потребуется Bubble Sort, чтобы отсортировать текущий массив.
 * <p>
 * Тема:
 * - Дерево отрезков
 * - Bubble Sort
 * - Поддержка ответа после каждого обновления
 * <p>
 * Идея:
 * 1) Bubble Sort делает столько проходов, сколько максимум единиц стоит перед каким-то нулём,
 *    плюс ещё один последний проход без обменов.
 * 2) Массив рассматривается как бинарный:
 *    - 1 — позиция уже занята,
 *    - 0 — позиция ещё пуста.
 * 3) В дереве отрезков храним:
 *    - ones[v] — сколько единиц на отрезке,
 *    - best[v] — максимум единиц перед нулём внутри отрезка.
 * 4) После каждого update(pos) пересчитываем ответ в корне.
 * <p>
 * Почему работает:
 * - За один проход пузырька каждый ноль может сдвинуться влево
 *   максимум через одну группу единиц.
 * - Значит число проходов определяется максимальным количеством единиц,
 *   стоящих перед некоторым нулём.
 * - Дерево отрезков позволяет быстро поддерживать этот максимум после каждого изменения.
 * <p>
 * Асимптотика:
 * Одно обновление: O(log n)
 * Все обновления: O(n log n)
 * Память: O(n)
 * <p>
 * Важно:
 * - NEG означает, что на отрезке нет нуля, перед которым можно считать answer.
 * - В корне best[1] хранится максимум единиц перед нулём по всему массиву.
 * - Итоговый ответ: best[1] + 1, потому что нужен ещё финальный проход без обменов.
 * ===========================================================
 */
public class YesBubbleSort {
    // Константа, обозначающая отсутствие нулей на отрезке
    private static final int NEG = -1;

    // Дерево отрезков.
    // Ones[v] хранит количество единиц на отрезке v.
    // Best[v] хранит максимальное количество единиц перед нулем на отрезке v.
    private static int[] ones;
    private static int[] best;

    public static void main(String[] args) throws Exception {
        FastScanner fs = new FastScanner(System.in);
        int n = (int) fs.nextLong();

        // Размер массива дерева обычно берется 4 * N для запаса
        ones = new int[4 * n + 5];
        best = new int[4 * n + 5];

        StringBuilder sb = new StringBuilder();

        // Для пустого массива ответ всегда 1 (сразу финальный проход)
        sb.append(1);

        for (int i = 0; i < n; i++) {
            int pos = (int) fs.nextLong(); // Получаем позицию вставки от 1 до n
            update(1, 1, n, pos);

            // Смотрим результат в корне дерева (индекс 1)
            int rootBest = best[1];

            // Если нулей вообще нет (вернулся NEG), значит единиц перед нулем 0
            int maxOnesBeforeZero = (rootBest == NEG) ? 0 : rootBest;

            // Итоговый ответ — это максимум единиц перед нулем + 1
            int passes = maxOnesBeforeZero + 1;

            sb.append(' ').append(passes);
        }

        sb.append('\n');
        System.out.print(sb);
    }

    /**
     * Рекурсивное обновление дерева отрезков.
     * @param v текущая вершина дерева
     * @param left левая граница отрезка вершины
     * @param rigth правая граница отрезка вершины
     * @param pos позиция, куда нужно поставить единицу
     */
    private static void update(int v, int left, int rigth, int pos) {
        // Базовый случай: дошли до конкретного элемента (листа)
        if (left == rigth) {
            ones[v] = 1;     // Ставим единицу
            best[v] = NEG;   // Нулей на этом отрезке (длиной 1) больше нет, ставим маркер NEG
            return;
        }

        // Находим середину отрезка
        int mid = (left + rigth) / 2;

        // Решаем, в какую ветку спускаться
        if (pos <= mid) {
            // Идем в левого ребенка (индекс v * 2)
            update(v * 2, left, mid, pos);
        } else {
            // Идем в правого ребенка (индекс v * 2 + 1)
            update(v * 2 + 1, mid + 1, rigth, pos);
        }

        // После обновления детей, пересчитываем значения для текущего узла-родителя
        pull(v);
    }

    /**
     * Пересчет значений узла на основе его дочерних элементов (левого и правого).
     * Это ключевой метод алгоритма.
     * @param v текущая вершина дерева
     */
    private static void pull(int v) {
        int left = v * 2;       // Индекс левого ребенка
        int right = v * 2 + 1;  // Индекс правого ребенка

        // Общее число единиц на отрезке = единицы слева + единицы справа
        ones[v] = ones[left] + ones[right];

        // Ищем лучший ответ (максимум единиц перед нулем).
        // Кандидат справа вычисляется так:
        // Если справа нет нулей (NEG), то кандидат тоже NEG.
        // Иначе: это все единицы из левой половины ПЛЮС лучший ответ внутри правой половины.
        // Итоговый ответ для текущего узла — максимум между лучшим ответом слева и кандидатом справа
        best[v] = Math.max(best[left], (best[right] == NEG) ? NEG : ones[left] + best[right]);
    }

    /**
     * Класс для быстрого чтения данных (быстрее, чем стандартный Scanner).
     * Стандартная практика в спортивном программировании.
     */
    private static final class FastScanner {
        private final InputStream in;
        private final byte[] buffer = new byte[1 << 16];
        private int ptr = 0, len = 0;

        FastScanner(InputStream in) { this.in = in; }

        private int readByte() throws IOException {
            if (ptr >= len) {
                len = in.read(buffer);
                ptr = 0;
                if (len <= 0) return -1;
            }
            return buffer[ptr++];
        }

        long nextLong() throws IOException {
            int c;
            do {
                c = readByte();
                if (c == -1) return Long.MIN_VALUE;
            } while (c <= ' ');

            long sign = 1;
            if (c == '-') {
                sign = -1;
                c = readByte();
            }

            long val = 0;
            while (c > ' ') {
                val = val * 10 + (c - '0');
                c = readByte();
            }
            return val * sign;
        }
    }
}